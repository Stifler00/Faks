        <h1>Žena (84) u 2 ujutro partijala u stanu u Bjelovaru, susjedi pozvali policiju</h1>
        <figure>
        <img src="slike/slika.jpg" alt="slika" title="slika" height="300">
        </figure>
        <p>BJELOVARSKA je policija sinoć morala intervenirati u jednom stanu nakon što je zaprimila dojavu o narušavanju javnog reda i mira. Naime, iz jednog se stana u centru grada čula glasna muzika od koje susjedi nisu mogli spavati te su pozvali policiju.</p>
        <p>Po dolasku &nbsp; &nbsp; &nbsp; &nbsp; na mjesto događaja oko 2:30, policajci su u stanu zatekli 84-godišnjakinju koja je u tom trenutku bila sama i puštala glasnu muziku. Kako javlja policija, time je "ometala noćni mir drugih stanara u zgradi".</p>
